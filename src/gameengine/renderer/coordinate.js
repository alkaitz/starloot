/**
 * @author alkaitz
 * @param x - X coord
 * @param y - Y coord
*/
function Coordinate2D(x, y){
	this.x = x || 0;
	this.y = y || 0;
}

/**
 * @author alkaitz
 * @param x - X coord
 * @param y - Y coord
*/
function Size2D(x, y){
	this.x = x || 0;
	this.y = y || 0;
}

/**
 * @author alkaitz
 * @param x - X coord
 * @param y - Y coord
 * @param z - Z coord
*/
function Coordinate3D(x, y, z){
	this.x = x || 0;
	this.y = y || 0;
	this.z = z || 0;
}
